    public WaitingRoom(boolean buttonLaunch, DataGameCreation dataGameCreation, MainMenu mainMenu) {
        initComponents();
        if (!buttonLaunch) {
            this.jButton1.setSize(0, 0);
            this.jButton1.setVisible(false);
        }

        this.jTextArea1.setText(dataGameCreation.toStringInfos());

        infoConnection = new InfoConnection(MainMenu.pseudo, "WaitingRoom");
        System.out.println("Pseudo : " + infoConnection.getPseudo());

        try {
            
            this.mainMenu = mainMenu;
            
            socket = new Socket();
            socket.connect(new InetSocketAddress("localhost", 50000));

            try {
                outputStream = new ObjectOutputStream(socket.getOutputStream());
            } catch (IOException ex) {
                System.out.println("Problème pour ouvrir le out");
            }
            try {
                inputStream = new ObjectInputStream(socket.getInputStream());
            } catch (IOException ex) {
                System.out.println("Problème pour avoir le in");
            }

            listener = new ListenerWaitingRoom(this, inputStream, outputStream);
            listener.start();

            try {
                //We send the data to create the appropriate server connection
                outputStream.writeObject(infoConnection);
                outputStream.reset();
                //We send the data about the game
                outputStream.writeObject(dataGameCreation);
                outputStream.reset();
            } catch (IOException ex) {
                Logger.getLogger(WaitingRoom.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (IOException ex) {
            Logger.getLogger(WaitingRoom.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

