    public void envoyerMessage(Object message) {
        try {
            outputStream.writeObject(message);
            outputStream.reset();
        } catch (IOException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    

